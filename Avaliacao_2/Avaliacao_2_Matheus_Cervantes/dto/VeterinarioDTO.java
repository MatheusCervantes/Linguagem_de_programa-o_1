package br.com.avaliacao_2.dto;

public class VeterinarioDTO {
    private int id_vet, numero_vet;
    private String nome_vet, crmv_vet, rg_vet, logradouro_vet, bairro_vet, cidade_vet, estado_vet, cep_vet;

    public int getId_vet() {
        return id_vet;
    }

    public void setId_vet(int id_vet) {
        this.id_vet = id_vet;
    }

    public int getNumero_vet() {
        return numero_vet;
    }

    public void setNumero_vet(int numero_vet) {
        this.numero_vet = numero_vet;
    }

    public String getNome_vet() {
        return nome_vet;
    }

    public void setNome_vet(String nome_vet) {
        this.nome_vet = nome_vet;
    }

    public String getCrmv_vet() {
        return crmv_vet;
    }

    public void setCrmv_vet(String crmv_vet) {
        this.crmv_vet = crmv_vet;
    }

    public String getRg_vet() {
        return rg_vet;
    }

    public void setRg_vet(String rg_vet) {
        this.rg_vet = rg_vet;
    }

    public String getLogradouro_vet() {
        return logradouro_vet;
    }

    public void setLogradouro_vet(String logradouro_vet) {
        this.logradouro_vet = logradouro_vet;
    }

    public String getBairro_vet() {
        return bairro_vet;
    }

    public void setBairro_vet(String bairro_vet) {
        this.bairro_vet = bairro_vet;
    }

    public String getCidade_vet() {
        return cidade_vet;
    }

    public void setCidade_vet(String cidade_vet) {
        this.cidade_vet = cidade_vet;
    }

    public String getEstado_vet() {
        return estado_vet;
    }

    public void setEstado_vet(String estado_vet) {
        this.estado_vet = estado_vet;
    }

    public String getCep_vet() {
        return cep_vet;
    }

    public void setCep_vet(String cep_vet) {
        this.cep_vet = cep_vet;
    }

    
}
